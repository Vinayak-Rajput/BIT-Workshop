#include<iostream>
#include<cmath>
using namespace std;
int main()
{
    int a=10,b =20;
    cout<<"Minimum: "<<min(a,b)<<endl;
    cout<<"Maximum: "<<max(a,b)<<endl;
    cout<<"Absolute: "<<abs(a-b)<<endl;
    cout<<"Square root: "<<sqrt(a)<<endl;
    cout<<"Power: "<<pow(a,b)<<endl;
    cout<<"Exponential: "<<exp(a)<<endl;
    cout<<"Logarithm: "<<log(a)<<endl;
    cout<<"Logarithm base 10: "<<log10(a)<<endl;
    cout<<"Ceil: "<<ceil(3.14)<<endl;
    cout<<"Floor: "<<floor(3.14)<<endl;
    cout<<"Round: "<<round(3.14)<<endl;
    cout<<"Truncate: "<<trunc(3.14)<<endl;
    cout<<"Sine: "<<sin(3.14)<<endl;
    cout<<"Cosine: "<<cos(3.14)<<endl;
    cout<<"Tangent: "<<tan(3.14)<<endl;
    cout<<"Arc sine: "<<asin(0.5)<<endl;
    cout<<"Arc cosine: "<<acos(0.5)<<endl;
    cout<<"Arc tangent: "<<atan(0.5)<<endl;
    cout<<"Hyperbolic sine: "<<sinh(3.14)<<endl;
    cout<<"Hyperbolic cosine: "<<cosh(3.14)<<endl;
    cout<<"Hyperbolic tangent: "<<tanh(3.14)<<endl;
    cout<<"Hyperbolic arc sine: "<<asinh(0.5)<<endl;
    cout<<"Hyperbolic arc cosine: "<<acosh(1.5)<<endl;
    cout<<"Hyperbolic arc tangent: "<<atanh(0.5)<<endl;
    return 0;
}