#include<iostream>
using namespace std;
int main()
{
    char c='A';
    cout<<"c: "<<c<<endl;
    cout<<"c+1: "<<c+1<<endl;
    cout<<"c-1: "<<c-1<<endl;
    cout<<"c*2: "<<c*2<<endl;
    cout<<"c/2: "<<c/2<<endl;
    cout<<"c%2: "<<c%2<<endl;
    cout<<"c<<2: "<<(c<<2)<<endl;
    cout<<"c>>2: "<<(c>>2)<<endl;
    cout<<"c&2: "<<(c&2)<<endl;
    cout<<"c|2: "<<(c|2)<<endl;
    cout<<"c^2: "<<(c^2)<<endl;
    cout<<"~c: "<<(~c)<<endl;
    
    return 0;
}