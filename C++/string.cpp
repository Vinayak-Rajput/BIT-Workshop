#include<iostream>
using namespace std;
int main()
{
    string s1="Hello", s2="World";
    cout<<"s1: "<<s1<<endl;
    cout<<"s2: "<<s2<<endl;
    cout<<"s1+s2: "<<s1+s2<<endl;
    cout<<"s1==s2: "<<(s1==s2)<<endl;
    cout<<"s1!=s2: "<<(s1!=s2)<<endl;
    cout<<"s1>s2: "<<(s1>s2)<<endl;
    cout<<"s1<s2: "<<(s1<s2)<<endl;
    cout<<"s1>=s2: "<<(s1>=s2)<<endl;
    cout<<"s1<=s2: "<<(s1<=s2)<<endl;
    return 0;
}